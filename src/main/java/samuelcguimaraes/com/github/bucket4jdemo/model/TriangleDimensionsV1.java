package samuelcguimaraes.com.github.bucket4jdemo.model;

public class TriangleDimensionsV1 {
	final Double height;
	final Double base;
	
	public TriangleDimensionsV1(Double height, Double base) {
		this.height = height;
		this.base = base;
	}
	
	public Double getHeight() {
		return height;
	}
	
	public Double getBase() {
		return base;
	}
}
