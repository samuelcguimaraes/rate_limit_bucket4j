package samuelcguimaraes.com.github.bucket4jdemo.model;

public class RectangleDimensionsV1 {
	final Double length;
	final Double width;
	
	public RectangleDimensionsV1(Double length, Double width) {
		this.length = length;
		this.width = width;
	}
	
	public Double getLength() {
		return length;
	}
	
	public Double getWidth() {
		return width;
	}
}
